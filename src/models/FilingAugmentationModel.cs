namespace Avalara.AvaTax.RestClient
{
    /// <summary>
    /// A model for return augmentations.
    /// </summary>
    public class FilingAugmentationModel
    {
        /// <summary>
        /// The unique ID number for the augmentation.
        /// </summary>
        public Int64? id { get; set; }

        /// <summary>
        /// The filing return id that this applies too
        /// </summary>
        public Int64? filingId { get; set; }

        /// <summary>
        /// The field amount.
        /// </summary>
        public Decimal fieldAmount { get; set; }

        /// <summary>
        /// The field name.
        /// </summary>
        public String fieldName { get; set; }

        /// <summary>
        /// The date when this record was created.
        /// </summary>
        public DateTime? createdDate { get; set; }

        /// <summary>
        /// The User ID of the user who created this record.
        /// </summary>
        public Int32? createdUserId { get; set; }

        /// <summary>
        /// The date/time when this record was last modified.
        /// </summary>
        public DateTime? modifiedDate { get; set; }

        /// <summary>
        /// The user ID of the user who last modified this record.
        /// </summary>
        public Int32? modifiedUserId { get; set; }


        /// <summary>
        /// Convert this object to a JSON string of itself
        /// </summary>
        /// <returns>A JSON string of this object</returns>
        public override string ToString()
        {
            return JsonConvert.SerializeObject(this, new JsonSerializerSettings() { Formatting = Formatting.Indented });
        }
    }
}
